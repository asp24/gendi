package generator

import (
	"bytes"
	"fmt"
	"go/format"

	di "github.com/asp24/gendi"
)

// Options holds rendering-specific options for code generation.
type Options struct {
	Package       string
	Container     string
	BuildTags     string
	OutputPkgPath string
}

type Generator struct {
	identGenerator *IdentGenerator
}

func NewGenerator(identGenerator *IdentGenerator) *Generator {
	return &Generator{identGenerator: identGenerator}
}

func (g *Generator) assembleOutput(options Options, importManager *ImportManager, body *bytes.Buffer) []byte {
	out := &bytes.Buffer{}

	// Build tags
	if options.BuildTags != "" {
		fmt.Fprintf(out, "//go:build %s\n", options.BuildTags)
		fmt.Fprintf(out, "// +build %s\n\n", options.BuildTags)
	}

	// Package and imports
	fmt.Fprintln(out, "// Code generated by gendi; DO NOT EDIT.")
	fmt.Fprintf(out, "package %s\n\n", options.Package)

	extraImports := []string{"sync", "fmt", "github.com/asp24/gendi/parameters"}
	out.WriteString(importManager.renderImports(extraImports))
	out.Write(body.Bytes())

	return out.Bytes()
}

func (g *Generator) Generate(options Options, cfg *di.Config, ctx *GenContext) ([]byte, error) {
	getterRegistry := NewGetterRegistry(g.identGenerator)
	importManager := NewImportManager(options.OutputPkgPath)
	inliner := NewInlinerComposite(
		NewInlinerMakeSlice(importManager),
		NewInlinerMakeChan(importManager),
	)

	body := &bytes.Buffer{}

	pRenderer := NewParametersRenderer(importManager)
	if err := pRenderer.Render(cfg.Parameters, options.Container, body); err != nil {
		return nil, err
	}

	cRenderer := NewContainerRenderer(importManager, g.identGenerator, getterRegistry, inliner, options.Container)
	if err := cRenderer.Render(cfg, ctx, body); err != nil {
		return nil, err
	}

	code := g.assembleOutput(options, importManager, body)
	formatted, err := format.Source(code)
	if err != nil {
		return nil, fmt.Errorf("format generated code: %w", err)
	}

	return formatted, nil
}
